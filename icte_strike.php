		<h1>The concept</h1>
		<p>
		After the succesfull <a href=http://arch.icte.uowm.gr/game/uowm_masacre/> UOWM Massacre: The Game </a> another team of students decided
		to create an improved FPS game, using a different 3D engine. This is the outcome... </p>

		ICTE Strike is a free First Person Shooter game made by two undergraduate students of University of Western Macedonia,
		in the free unreal engine 4.6 machine, which facilitates 3D game engineering,
		under the supervision of <a href=http://arch.icte.uowm.gr/mdasygenis>Dr. Minas Dasygenis</a>. Note that in our Department there is no course on Game Development, and thus
		all this storyboard, graphics, programming was done by the students under supervision of their professor, who is a researcher on Embedded System Design.<br>
		<p>
		
		In this game, you can play either with a number of bots (from 1 to 8), or with other humans, in two game modes: free for all and team deathmatch. 
		All the maps are on an <span class="red-text">accurate</span> scale of the real ones. Detailed measurements were made on all elements of the main building, as well
		as all the levels. In the building you can find 4 levels: the basement level which is the garage, the ground level which is a supermarket, the
		first level which belongs to the Department and the second level which is the roof.
		<p>

		In order to make it more appropriate for the gameplay, we inserted two ladders for the second level and we created in each level one more entry in order to have
		at least two entry points in each level.



